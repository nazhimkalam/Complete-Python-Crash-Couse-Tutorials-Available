#CASTING
#If you need to return a specific data type from the input data, we use casting

#Example 01
x = int(1)    # x will be 1
y = int(2.8)  # y will be 2
z = int("3")  # z will be 3
a = int(True) # a will be 1
b = int(False)# b will be 0

print('This is the value of x:',x)
print('This is the value of y:',y)
print('This is the value of z:',z)
print('This is the value of a:',a)
print('This is the value of b:',b)
print()

#Example 02
c = float(1)     # c will be 1.0
d = float(2.8)   # d will be 2.8
e = float("3")   # e will be 3.0
f = float("4.2") # f will be 4.2
g = float(True)  # g will be 1.0
h = float(False) # h will be 0.0

print('This is the value of c:',c)
print('This is the value of d:',d)
print('This is the value of e:',e)
print('This is the value of f:',f)
print('This is the value of g:',g)
print('This is the value of h:',h)
print()

#Example 03
